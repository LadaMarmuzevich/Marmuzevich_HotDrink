public class HotDrink {
    String name;
    int volume;
    
    public HotDrink(String name, int volume) {
        this.name = name;
        this.volume = volume;
    }

    public String getNamename() {
        return name;
    }

    public void setNamename(String name) {
        this.name = name;
    }
    public int getVolume() {
        return volume;
    }

    public void setVolume(int volume) {
        this.volume = volume;
    }

}
